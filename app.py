def generar_escenario_gemini(nivel):
    """ Genera 5 escenarios √∫nicos para el nivel dado usando Gemini via HTTP """
    prompt = f"""
    Eres un experto en √©tica empresarial y dise√±o de simulaciones interactivas.
    Genera EXACTAMENTE 5 escenarios √∫nicos y distintos de crisis empresariales en espa√±ol para un nivel de dificultad '{nivel}'.
    Cada escenario debe incluir:
    - 'id': Un identificador √∫nico y corto (ej: 'p1', 'p2' para principiante; 'i1', 'i2' para intermedio; 'a1', 'a2' para avanzado). Usa el prefijo correcto para el nivel ({nivel[0].lower()}).
    - 'titulo': Un t√≠tulo corto, atractivo y descriptivo en espa√±ol (m√°x 10 palabras).
    - 'trasfondo': Una descripci√≥n detallada (100-150 palabras) de la empresa ficticia, el contexto del mercado, el inicio de la crisis y el rol espec√≠fico que asume el jugador en la simulaci√≥n. Debe estar en espa√±ol.
    - 'estado_inicial': Un diccionario fijo: {{"financiera": 0, "reputacion": 0, "laboral": 0}}.

    Aseg√∫rate de que los escenarios sean apropiados para la dificultad indicada (Principiante: dilemas directos; Intermedio: ambig√ºedad, pros/contras; Avanzado: sist√©mico, multi-agente, largo plazo).

    Presenta la respuesta final EXCLUSIVAMENTE como una lista JSON v√°lida de estos 5 diccionarios. No incluyas ning√∫n otro texto antes o despu√©s de la lista JSON.
    """
    with st.spinner(f"üß† Generando escenarios ({nivel})..."):
        generated_text = make_gemini_request(prompt, GEMINI_API_KEY)
        if not generated_text:
            return [] # Error manejado en make_gemini_request

        parsed_response = parse_gemini_json_response(generated_text)

        # Resto de la validaci√≥n (igual que antes)
        if parsed_response and isinstance(parsed_response, list) and len(parsed_response) == 5:
                validated_scenarios = []
                for i, sc in enumerate(parsed_response):
                    if isinstance(sc, dict) and all(k in sc for k in ['id', 'titulo', 'trasfondo', 'estado_inicial']):
                        # Asegurar IDs √∫nicos si Gemini falla
                        if 'id' not in sc or not sc['id']:
                                sc['id'] = f"{nivel[0].lower()}{i+1}_{random.randint(1000,9999)}"
                        # Asegurar que el ID empiece con la letra correcta
                        if not sc['id'].startswith(nivel[0].lower()):
                            sc['id'] = f"{nivel[0].lower()}{sc['id'].lstrip('pia')}" # Intenta corregir prefijo

                        if not isinstance(sc['estado_inicial'], dict) or not all(k in sc['estado_inicial'] for k in ['financiera', 'reputacion', 'laboral']):
                                sc['estado_inicial'] = {"financiera": 0, "reputacion": 0, "laboral": 0} # Corregir si es necesario
                        validated_scenarios.append(sc)
                    else:
                        st.warning(f"Escenario {i+1} recibido de Gemini no tiene el formato esperado. Omitiendo.")
                if len(validated_scenarios) == 5:
                    st.success(f"‚úÖ ¬°5 escenarios ({nivel}) generados!")
                    return validated_scenarios
                else:
                    st.error(f"Error: Se esperaban 5 escenarios v√°lidos, pero se obtuvieron {len(validated_scenarios)}. Verifica el prompt y la respuesta de Gemini.")
                    return []
        elif parsed_response:
                st.error(f"Error: Se esperaba una lista de 5 elementos, pero se recibi√≥ otra estructura.")
                st.json(parsed_response)
                return []
        else:
                # Error ya mostrado por parse_gemini_json_response
                return []

def generar_pregunta_y_opciones_gemini(contexto, historial, estado, nivel, numero_pregunta):
    """ Genera la siguiente pregunta y opciones usando Gemini via HTTP """
    historial_str = json.dumps(historial[-2:], ensure_ascii=False) # √öltimas 2 decisiones

    prompt = f"""
    Act√∫a como el director experto de una simulaci√≥n interactiva de crisis empresarial en espa√±ol.
    Nivel: {nivel}. Pregunta: {numero_pregunta}/10. Estado: Fin={estado['financiera']}, Rep={estado['reputacion']}, Lab={estado['laboral']}.
    Contexto: {contexto}
    Historial reciente: {historial_str}

    Genera la SIGUIENTE pregunta cr√≠tica (concisa, relevante, dilema claro) y EXACTAMENTE 4 opciones de respuesta (distintas, plausibles, prefijo A/B/C/D).

    Presenta la respuesta final EXCLUSIVAMENTE como un objeto JSON v√°lido con claves 'pregunta' (string) y 'opciones' (lista de 4 strings). No incluyas texto adicional.
    """
    with st.spinner(f"üß† Generando pregunta {numero_pregunta}..."):
        generated_text = make_gemini_request(prompt, GEMINI_API_KEY)
        if not generated_text:
            return "Pregunta no disponible (Error API)", []

        parsed_response = parse_gemini_json_response(generated_text)

        if parsed_response and isinstance(parsed_response, dict) and \
           'pregunta' in parsed_response and 'opciones' in parsed_response and \
           isinstance(parsed_response['opciones'], list) and len(parsed_response['opciones']) == 4:
            # Validar prefijos A) B) C) D) si es necesario
            return parsed_response['pregunta'], parsed_response['opciones']
        else:
            st.error("Error: Formato inesperado para pregunta/opciones.")
            if parsed_response: st.json(parsed_response)
            return f"Error al generar pregunta {numero_pregunta}. ¬øContinuar?", ["A) S√≠", "B) No", "C) Intentar de nuevo", "D) Salir"]


def evaluar_decision_gemini(contexto, pregunta, opcion_elegida, estado_actual, nivel, numero_pregunta):
    """ Eval√∫a la decisi√≥n, genera an√°lisis/consecuencias y nuevo contexto via HTTP """
    prompt = f"""
    Eres un analista experto evaluando una decisi√≥n en una simulaci√≥n en espa√±ol.
    Nivel: {nivel}. Pregunta respondida: {numero_pregunta}/10.
    Contexto ANTES: {contexto}
    Pregunta: {pregunta}
    Decisi√≥n: {opcion_elegida}
    Estado ANTES: Fin={estado_actual['financiera']}, Rep={estado_actual['reputacion']}, Lab={estado_actual['laboral']}.

    Realiza estas tareas y presenta el resultado EXCLUSIVAMENTE como un √∫nico objeto JSON v√°lido con claves 'analisis', 'consecuencias_texto', 'impacto', 'nuevo_contexto':
    1.  'analisis': An√°lisis conciso (2-3 frases) de la decisi√≥n (implicaciones √©ticas/estrat√©gicas).
    2.  'consecuencias_texto': Descripci√≥n breve (1-2 frases) de efectos inmediatos probables.
    3.  'impacto': Diccionario JSON con impacto num√©rico M√ÅS PROBABLE en 'financiera', 'reputacion', 'laboral' (enteros, rango -3 a +3 t√≠pico). E.g., {{"financiera": -1, "reputacion": 0, "laboral": -1}}.
    4.  'nuevo_contexto': Nuevo p√°rrafo de contexto (50-100 palabras) describiendo la situaci√≥n DESPU√âS de la decisi√≥n y consecuencias.

    No incluyas texto adicional fuera del objeto JSON.
    """
    with st.spinner(f"üß† Analizando decisi√≥n {numero_pregunta}..."):
         # Usar un timeout m√°s largo para evaluaci√≥n si es necesario
        generated_text = make_gemini_request(prompt, GEMINI_API_KEY, timeout=150)
        if not generated_text:
            return "An√°lisis no disponible (error API).", "Consecuencias no disponibles (error API).", f"{contexto}\n\n(Error al procesar la √∫ltima decisi√≥n).", {"financiera": 0, "reputacion": 0, "laboral": 0}

        parsed_response = parse_gemini_json_response(generated_text)

        if parsed_response and isinstance(parsed_response, dict) and \
           all(k in parsed_response for k in ['analisis', 'consecuencias_texto', 'impacto', 'nuevo_contexto']) and \
           isinstance(parsed_response['impacto'], dict) and \
           all(k in parsed_response['impacto'] for k in ['financiera', 'reputacion', 'laboral']):
            # Validar tipos de impacto
            impacto = parsed_response['impacto']
            impacto_validado = {
                'financiera': int(impacto.get('financiera', 0)),
                'reputacion': int(impacto.get('reputacion', 0)),
                'laboral': int(impacto.get('laboral', 0))
            }
            return parsed_response['analisis'], parsed_response['consecuencias_texto'], parsed_response['nuevo_contexto'], impacto_validado
        else:
            st.error("Error: Formato inesperado para la evaluaci√≥n.")
            if parsed_response: st.json(parsed_response)
            return "An√°lisis no disponible (error formato).", "Consecuencias no disponibles (error formato).", f"{contexto}\n\n(Error al procesar la √∫ltima decisi√≥n).", {"financiera": 0, "reputacion": 0, "laboral": 0}


